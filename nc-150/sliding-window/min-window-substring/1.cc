/*Given two strings s and t, return the shortest substring of s such that every character in t,
 * including duplicates, is present in the substring. If such a substring does not exist, return an empty string "".
 * You may assume that the correct output is always unique.
 *
 * Example: Input: s = "OUZODYXAZV", t = "XYZ"
 *          Output: "YXAZ"
 *
 * This program solves the problem using brute force method. We make use of hash map to track frequency of characters in
 * 't' and frequencies of characters in every possible substring of 's'. The window is valid if frequency of characters
 * in substring of 's' are not lesser than the frequencies of characters in 't'. We track the minimum `window_len` for
 * each valid window.
 *
 * Time complexity - o(n^2)
 * Space complexity - O(m), where 'm' is the number of unique characters in the strings
 *
 * */

#include <climits>
#include <iostream>
#include <string>
#include <unordered_map>

using namespace std;

class Solution {
public:
  string minWindow(string s, string t) {
    unordered_map<char, int> countT;
    for (char c : t) {
      countT[c]++;
    }
    int window_len = INT_MAX;
    int window_start_index;

    for (int i = 0; i < s.size(); i++) {
      unordered_map<char, int> countS;
      for (int j = i; j < s.size(); j++) {
        countS[s[j]]++;

        bool valid_window = true;

        for (auto &[c, cnt] : countT) {
          if (countS[c] < cnt) {
            valid_window = false;
            break;
          }
        }

        if (valid_window && j - i + 1 < window_len) {
          window_len = j - i + 1;
          window_start_index = i;
        }
      }
    }
    if (window_len == INT_MAX)
      return "";
    return s.substr(window_start_index, window_len);
  };
};

